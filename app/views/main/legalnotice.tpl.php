            <h4 class="text-primary fw-bold">Objet du traitement</h4>

            <p>Pron'Autocross collecte et traite des données personnelles afin de permettre à chaque utilisateur d'avoir un espace personnel sur le site.</p>

            <p>La collecte et le traitement des données effectué par Pron'Autocross a pour base légale les <a href="https://www.cnil.fr/fr/reglement-europeen-protection-donnees/chapitre2#Article6">articles 6.1.a, 6.1.b et 6.1.f</a> du Réglement Européen pour la Protection des Données (RGPD).</p>

            <h4 class="text-primary fw-bold">Catégories de données</h4>

            <p>Les données à caractère personnel collectées sont les suivantes : nom, prénom, adresse électronique, code interne de traitement permettant l'identification de l'utilisateur.</p>

            <h4 class="text-primary fw-bold">Conditions de la collecte et durée de conservation des donnée</h4>

            <p>Les données à caractère personnel sont collectées dès la création d'un compte utilisateur sur le site et jusqu'à ce que le-dit compte utilisateur soit supprimé.</p>

            <h4 class="text-primary fw-bold">Destinataire des données</h4>

            <p>le site Pron'Autocross est destinataire de l'ensemble des catégories de données à caractère personnel collectées. Les données collectées ne sont en aucun cas transmises à des tiers.</p>

